public class Cliente {
    private int id;
    private int età;


    public Cliente(int id, int età) {
        this.id = id;
        this.età = età;
    }

    public int getId() {
        return this.id;
    }

    public int getEtà() {
        return this.età;
    }

    public void setEtà(int età) {
        this.età = età;
    }
 

}
